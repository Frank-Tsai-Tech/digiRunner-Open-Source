package tpi.dgrv4.dpaa.vo;

public class AA0021RoleInfo {

	private String roleName;
	
	private String roleAlias;
	
	private String roleAliasRoleName;

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getRoleAlias() {
		return roleAlias;
	}

	public void setRoleAlias(String roleAlias) {
		this.roleAlias = roleAlias;
	}

	public String getRoleAliasRoleName() {
		return roleAliasRoleName;
	}

	public void setRoleAliasRoleName(String roleAliasRoleName) {
		this.roleAliasRoleName = roleAliasRoleName;
	}

	
}
